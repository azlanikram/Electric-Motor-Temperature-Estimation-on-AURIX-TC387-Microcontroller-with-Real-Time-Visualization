static void delayMs(uint32_t ms)
{
    IfxStm_waitTicks(&MODULE_STM0,
    IfxStm_getTicksFromMilliseconds(&MODULE_STM0, ms));
}

static inline uint64 now(void)
{
    return IfxStm_get(STM_PTR);
}

static inline float elapsed(Ifx_TickTime start)
{
    Ifx_TickTime end = IfxStm_get(STM_PTR);
    float32 freq = IfxStm_getFrequency(STM_PTR);
    return (float)(end - start) / freq * 1000.0f;  // milliseconds
}

