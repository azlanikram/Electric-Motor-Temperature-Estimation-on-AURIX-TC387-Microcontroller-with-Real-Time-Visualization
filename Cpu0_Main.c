void userIsrSTM0()
{
    /* Set next 1ms scheduler tick alarm */
    IfxStm_increaseCompare(&MODULE_STM0, IfxStm_Comparator_0, IFX_CFG_STM_TICKS_PER_MS);

    /* Increment the ms value */
    g_AppCpu0.TickCount_1ms++;

    /* Enable the global interrupts of this CPU */
    IfxCpu_enableInterrupts();

    /* Return if Safety Kit initialization is not yet finished */
    if(!tft_ready)
    {
        return;
    }

    /* action to do each 1ms */
    /* Execute several periodic tasks implemented and used for Safety Kit */
   runStmIsr(&g_AppCpu0, (IfxCpu_ResourceCpu)IfxCpu_getCoreIndex());
}
